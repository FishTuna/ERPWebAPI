    public class OHS_InstructorListManager : IOHS_InstructorListService<OHS_InstructorList, SqlResult>
    {
        IOHS_InstructorListDal _oHS_InstructorListDal;

        public OHS_InstructorListManager(IOHS_InstructorListDal oHS_InstructorListDal)
        {
            _oHS_InstructorListDal = oHS_InstructorListDal;
        }


        //[CacheAspect]
        //[ValidationAspect(typeof(VacationTypeValidator))]
        //[PerformanceAspect(15)]
        public IDataResult<List<OHS_InstructorList>> GetAllDataMngr(string module, string target, string point, string parameters)
        {
            ///kurallar private mwthod olarak eklenecek aşağpıya
            //IDataResult<SqlResult> result = BusinessRules.Run();
            //if (result != null)
            //{
            //    return result;
            //}
            return new SuccessDataResult<List<OHS_InstructorList>>(_oHS_InstructorListDal.GetAllDataDal(module, target, point, parameters), Messages.Listed);
        }

        public IDataResult<SqlResult> ResultOperationsMngr(string module, string target, string point, string parameters)
        {
            var result = _oHS_InstructorListDal.ResultOperationsDal(module, target, point, parameters);
            if (!result.sqlReturn)
            {
                return new ErrorDataResult<SqlResult>(result);
            }
            return new SuccessDataResult<SqlResult>(result);
        }
    }
