namespace ERPWebAPI.BL.Concrete.OHS
{
    public class OHS_DocumentManager : IOHS_DocumentService<OHS_Document, SqlResult>
    {
        IOHS_DocumentDal _oHS_DocumentDal;

        public OHS_DocumentManager(IOHS_DocumentDal oHS_DocumentDal)
        {
            _oHS_DocumentDal = oHS_DocumentDal;
        }


        //[CacheAspect]
        //[ValidationAspect(typeof(VacationTypeValidator))]
        //[PerformanceAspect(15)]
        public IDataResult<List<OHS_Document>> GetAllDataMngr(string module, string target, string point, string parameters)
        {
            ///kurallar private mwthod olarak eklenecek aşağpıya
            //IDataResult<SqlResult> result = BusinessRules.Run();
            //if (result != null)
            //{
            //    return result;
            //}
            return new SuccessDataResult<List<OHS_Document>>(_oHS_DocumentDal.GetAllDataDal(module, target, point, parameters), Messages.Listed);
        }

        public IDataResult<SqlResult> ResultOperationsMngr(string module, string target, string point, string parameters)
        {
            var result = _oHS_DocumentDal.ResultOperationsDal(module, target, point, parameters);
            if (!result.sqlReturn)
            {
                return new ErrorDataResult<SqlResult>(result);
            }
            return new SuccessDataResult<SqlResult>(result);
        }
    }
}
